public class ch3_Escape_sequence {
    public static void main(String[] args) {
        System.out.println("David warner left handed opner \n batsman from australia");
        System.out.println("David warner left handed opner\tatsman from australia");
        System.out.println("David warner left handed opner \\ batsman from australia");
        System.out.println("David warner left handed opner \" batsman from australia");
    }
}
