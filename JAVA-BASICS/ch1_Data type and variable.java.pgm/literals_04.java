
public class literals_04 {
    public static void main(String[] args) {
        byte age = 43;
        short s1 = 90;
        int integer = 4543;
        long Long = 4535723423643674L;  // capital F or small f both are valid
        float Float = 5.34F;  // capital F or small f both are valid
        double Double = 5463.5633445; // in double we have optinonal d or D use or not
        char Character = 'a';
        boolean Bool = true;  // true or false both are valid
        System.out.println(age);
        java.lang.String str = "Khan"; // it is a special type of support we can see that next time
        System.out.println(str);
    }
}
