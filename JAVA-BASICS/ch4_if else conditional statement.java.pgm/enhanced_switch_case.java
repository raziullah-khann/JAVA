import java.util.Scanner;

public class enhanced_switch_case {
    public static void main(String[] args) {
       int a = 90;
       switch(a){
           case 10-> System.out.println("Your mark nice");
           case 80-> System.out.println("Your mark very good");
           case 90-> System.out.println("Your mark very exellent");
           default-> System.out.println("Your mark not good");

       }
    }
}
